import 'package:flutter/material.dart';

import '../../extensions/string_extensions.dart';

ThemeData defineTheme({
  required Brightness brightness,
  required String primary,
  required String onPrimary,
  required String primaryContainer,
  required String onPrimaryContainer,
  required String secondary,
  required String onSecondary,
  required String secondaryContainer,
  required String onSecondaryContainer,
  required String tertiary,
  required String onTertiary,
  required String tertiaryContainer,
  required String onTertiaryContainer,
  required String error,
  required String onError,
  required String errorContainer,
  required String onErrorContainer,
  required String background,
  required String onBackground,
  required String surface,
  required String onSurface,
  required String outline,
  required String surfaceVariant,
  required String onSurfaceVariant,
}) {
  return ThemeData(
    useMaterial3: true,
    colorScheme: ColorScheme.fromSeed(
      brightness: brightness,
      seedColor: primary.fromHex(),
      primary: primary.fromHex(),
      onPrimary: onPrimary.fromHex(),
      primaryContainer: primaryContainer.fromHex(),
      onPrimaryContainer: onPrimaryContainer.fromHex(),
      secondary: secondary.fromHex(),
      onSecondary: onSecondary.fromHex(),
      secondaryContainer: secondaryContainer.fromHex(),
      onSecondaryContainer: onSecondaryContainer.fromHex(),
      tertiary: tertiary.fromHex(),
      onTertiary: onTertiary.fromHex(),
      tertiaryContainer: tertiaryContainer.fromHex(),
      onTertiaryContainer: onTertiaryContainer.fromHex(),
      error: error.fromHex(),
      onError: onError.fromHex(),
      errorContainer: errorContainer.fromHex(),
      onErrorContainer: onErrorContainer.fromHex(),
      background: background.fromHex(),
      onBackground: onBackground.fromHex(),
      surface: surface.fromHex(),
      onSurface: onSurface.fromHex(),
      outline: outline.fromHex(),
      surfaceVariant: surfaceVariant.fromHex(),
      onSurfaceVariant: onSurfaceVariant.fromHex(),
    ),
    scaffoldBackgroundColor: background.fromHex(),
  );
}

